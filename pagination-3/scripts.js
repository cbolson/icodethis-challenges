// debounce to prevent calling the apis if the user is still typing
function debounce(func, delay = 1000) {
    let timeout;
    return (...args) => {
        clearTimeout(timeout);
        timeout = setTimeout(() => {
            func(...args);
        }, delay);
    };
}

const sendDebounce = debounce((term) => {
    searchItems(term);
}, 500);

