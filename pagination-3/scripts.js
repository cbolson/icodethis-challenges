// retrieve characters from api
async function searchItems(term) {
    toggleSpinner(true);
    try {
        const response = await fetch(`https://api.pexels.com/v1/search?query=${term}&per_page=80`, {
            headers: {
                'Content-Type': 'application/json',
                'Authorization': API_KEY
            }
        });
        const results = await response.json();
        data = results.photos;
       //console.log(data);
        curPage = 0;
        totalResults = data.length
        totalPages = numPages(totalResults);
        renderItems();
        toggleSpinner(false);

    } catch (error) {
        console.log(error);
    }
}


// add results to page
function renderItems() {
    listResults.innerHTML = '';
   // console.log(curPage);

    // define offset
    const offset = curPage * ITEMS_PER_PAGE;
    //console.log(offset);
    // get portioon or results we want to show
    const results = data.slice(offset, (offset + ITEMS_PER_PAGE));

    //console.log(results);
    if (results.length < 1) {
        listResults.innerHTML = '<p class="text-xl">No results</p>';
    } else {
        let delay = 100;

        results.forEach(photo => {
            const item = tplItem.content.cloneNode(true);
            item.querySelector("a").href = photo.url
            const img = item.querySelector("img");

            img.src = photo.src.portrait;
            img.setAttribute("alt", photo.alt);
            item.querySelector("[item-title]").innerText = photo.alt
            item.querySelector("[item-photographer]").innerText = photo.photographer
            listResults.append(item);

            delay = delay + 50;
            setTimeout(() => img.classList.add("scale-110"), delay);
        })
    }
    // define pagination
    pagination(curPage, offset)

}

function pagination(offset) { 
    pageNumbersList.innerHTML = '';
   // totalPages = Math.round(totalResults / ITEMS_PER_PAGE);
    const currentPage = Number(curPage) + 1 ;

    displayCurrentPage.innerText = `${currentPage} of ${totalPages}`

    // create numbers
    for (let i = 0; i < totalPages; i++) { 
       // console.log(i);
        const btn = document.createElement("button");
        btn.value = i;
        btn.innerText = i + 1;
        //console.log(i,currentPage)
        if (i == curPage) btn.classList.add("current");

        pageNumbersList.append(btn)
        btn.addEventListener("click", () => { 
            //console.log("load results page ", i);
            curPage = btn.value;
            renderItems()
        })

    }
    checkCurrentPage();
}

// disabled/enable buttons
function checkCurrentPage() {
    // prev button
    btnPrev.disabled = (curPage == 0) ? true : false;

    // next button
    btnNext.disabled = (curPage == totalPages - 1) ? true : false;
}

// jump to page
function goToPage(page){ 
   // console.log("go to page: ", page);
    switch (page) { 
        case "first":
            curPage = 0;
            break;
        case "prev":
            curPage = curPage > 0 ? curPage - 1 : 0;
            break;
        case "next":
             curPage = curPage < totalPages ? curPage + 1 : totalPages - 1;
            break;
        case "last":
            curPage = totalPages - 1;
            break;
    }
    renderItems();
}
